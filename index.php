<td class="text">
Anand Vihar of Shree Sansthan has got its own prestige for the most innovative and attractive
building structures of Bhakta Niwas.Here the accommodation simply appears like beautiful homes.
The sculpture at the grand entrance fascinates the visitors at once where all the great saints
of Maharashtra can be found with their most beloved God- Shri Hari Vitthal. All the sculptures
in Anand Vihar are literally outstanding with the enchanting beautiful look, which mesmerizes us
into the work of spirituality. Adding to this, the lovely pasture, specious ways, the attracting 
structure, the serene atmosphere,the lovely interior and amazing cleanliness suddenly takes us to 
a dream land of heaven.Shegaon and surrounding area has shortage of water and hence Shri Gajanan Maharaj Sansthan,
Shegaon felt the need of a lake with ample of water in Shegaon which would help to solve the scarcity of water.
With this aim Shree Sansthan created the artificial lake in Shegaon by lifting water from Man River (9 Km away from Shegaon)
into the lake of Anand Sagar. But the mothly expenditure Rs. 50 Lakh for this purpose caused financial burden on Sansthan.
 Even then, the Sansthan carried out the project of Anand Sagar Lake, in order to increase the water table in surrouding 
 areas of Shegaon. Not only this, the Shree Sansthan decided to develop this lake and its surrounding area with unique
 combination of spirituality and Amusement park for devotees with nominal donation. And the revenue from it would help 
 to deal with water problem. With this noble vision and objective Shree Sansthan's most innovative project- Anand Sagar 
 came into exbtance with Shree's blessings. 
